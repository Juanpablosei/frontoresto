export const useThemeStore = create<ThemeStore>()(
  persist(
    (set, get) => ({
      currentPalette: defaultPalettes.amber,
      availablePalettes: defaultPalettes,
      selectedPaletteName: 'amber',

      setPalette: (paletteName: string) => {
        const palette = defaultPalettes[paletteName];
        if (palette) {
          set({
            currentPalette: palette,
            selectedPaletteName: paletteName,
          });
        }
      },

      getCurrentPalette: () => {
        return get().currentPalette;
      },

      getPaletteNames: () => {
        return Object.keys(defaultPalettes);
      },
    }),
    {
      name: 'theme-storage',
    }
  )
);
